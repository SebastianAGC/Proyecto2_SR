    private void btnRecomendacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRecomendacionActionPerformed
        // TODO add your handling code here:
                double costo=0;
        for(CPU c : miControl.CpuList){
            if(cbx_cpu.getSelectedItem().toString().equals(c.toString())){
                costo=costo+c.getPrecio();
            }
        }
        for(Case c : miControl.CaseList){
            if(cbx_case.getSelectedItem().toString().equals(c.toString())){
                costo=costo+c.getPrecio();
            }
        }
        for(GPU g : miControl.GpuList){
            if(cbx_gpu.getSelectedItem().toString().equals(g.toString())){
                costo=costo+g.getPrecio();
            }
        }
        for(Motherboard m : miControl.MoboList){
            if(cbx_mobo.getSelectedItem().toString().equals(m.toString())){
                costo=costo+m.getPrecio();
            }
        }
        for(Ram r : miControl.RamList){
            if(cbx_ram.getSelectedItem().toString().equals(r.toString())){
                costo=costo+r.getPrecio();
            }
        }
        for(discoDuro d : miControl.discoDuroList){
            if(cbx_discoDuro.getSelectedItem().toString().equals(d.toString())){
                costo=costo+d.getPrecio();
            }
        }
        for(powerSupply p : miControl.PSUList){
            if(cbx_fuente.getSelectedItem().toString().equals(p.toString())){
                costo=costo+p.getPrecio();
            }
        }
        
        txtCosto.setText(""+round(costo, 2));
    }//GEN-LAST:event_btnRecomendacionActionPerformed

