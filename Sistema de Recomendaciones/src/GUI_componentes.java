    private void cbx_moboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbx_moboActionPerformed
        // TODO add your handling code here:
        String CASE="";
        //cbx_case.removeAllItems();
        for(Motherboard m : miControl.MoboList){
            if(cbx_mobo.getSelectedItem().toString().equals(m.toString())){        
              CASE=m.getTipo();
            }
        }
        cbx_case.removeAllItems();
        for(Case c: miControl.CaseList){
            if(c.getTipoMobo().equals(CASE)){
                cbx_case.addItem(c.toString());
            }
        }
        
        String SOCKET="";
        for(Motherboard m : miControl.MoboList){
            if(cbx_mobo.getSelectedItem().toString().equals(m.toString())){
                SOCKET=m.getSocket();
            }
        }
        cbx_cpu.removeAllItems();
        for(CPU c : miControl.CpuList){
            if(c.getSocket().equals(SOCKET)){
                cbx_cpu.addItem(c.toString());
            }
        }
        
        String PCI = "";
        //
        for(Motherboard m : miControl.MoboList){
            if(cbx_mobo.getSelectedItem().toString().equals(m.toString())){
                PCI=m.getPuertoPCI();
            }
        }
        cbx_gpu.removeAllItems();
        for(GPU g : miControl.GpuList){
            if(g.getTipoPCI().equals(PCI)){
                cbx_gpu.addItem(g.toString());
            }
        }
        
        String RAM="";
        //
        for(Motherboard m : miControl.MoboList){
            if(cbx_mobo.getSelectedItem().toString().equals(m.toString())){
                RAM=m.getTipoRam();
            }
        }
        cbx_ram.removeAllItems();
        for(Ram r : miControl.RamList){
            if(r.getTipo().equals(RAM)){
                cbx_ram.addItem(r.toString());
            }
        }
        //System.out.println("JUEM");
    }//GEN-LAST:event_cbx_moboActionPerformed

